// Helper functions
export const getAchievementsByCategory = (category: string) => {
  return achievementsData.filter(achievement => achievement.category === category);
};

export const getUnlockedAchievements = (achievements: Achievement[]) => {
  return achievements.filter(achievement => achievement.unlocked);
};

export const getAchievementProgress = (achievement: Achievement) => {
  return achievement.maxProgress > 0 ? (achievement.progress / achievement.maxProgress) * 100 : 0;
};

export const calculateTotalXpFromAchievements = (achievements: Achievement[]) => {
  return achievements.reduce((total, achievement) => {
    return total + (achievement.unlocked ? achievement.xpReward : 0);
  }, 0);
};